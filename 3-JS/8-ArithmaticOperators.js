console.log("=> Arithmatic Operators:");

let x = 20;
let y = 10;

console.log("X + Y = " + (x + y));     // addition
console.log("X - Y = " + (x - y));     // subtraction
console.log("X * Y = " + (x * y));     // multiplication
console.log("X / Y = " + (x / y));     // division
console.log("X % Y = " + (x % y));     // modulus
console.log("++X = " + ++x);     // prefix increment
console.log("Y++ = " + y++);     // Postfix increment
console.log("--X = " + --x);     // Prefix decrement
console.log("Y-- = " + y--);     // Postfix decrement

let num = 5;
console.log("Num ** 2 = " + num ** 2);
console.log("Math.pow(Num, 2) = " + Math.pow(num, 2));